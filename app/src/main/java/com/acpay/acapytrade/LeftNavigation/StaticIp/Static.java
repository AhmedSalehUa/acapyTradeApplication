package com.acpay.acapytrade.LeftNavigation.StaticIp;

public class Static {
    String place;
    String ip;

    public Static(String place, String ip) {
        this.place = place;
        this.ip = ip;
    }

    public String getPlace() {
        return place;
    }

    public void setPlace(String place) {
        this.place = place;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }
}
